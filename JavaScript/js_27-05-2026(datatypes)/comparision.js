//primitive data type
//number
let a = 10
let b = a
b = 50
console.log(a)
console.log(b)

//string
let x = "Hello"
let y = x
y = "JavaScript"
console.log(x)
console.log(y)

//boolean
let online = true
let status = online
status = false
console.log(online)
console.log(status)

//non primitive  data type
//array
let arr1 = [10,20]
let arr2 = arr1
arr2.push(30)
console.log(arr1)
console.log(arr2)

//object
let person1 = {
    name : "Girish"
}
let person2 = person1
person2.name = "Ravi"
console.log(person1.name)
console.log(person2.name)

