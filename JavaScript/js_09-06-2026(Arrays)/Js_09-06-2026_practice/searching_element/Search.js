//indexOf it will return the first index of specfied element
//if the element is not found it will get -1
let numbers=[10,20,30,20,40]
console.log(numbers.indexOf(20))
console.log(numbers.indexOf(50))
console.log(numbers.length)

//lastindexof it will return the last occurrence of the specified element.
let numbers1=[10,20,30,20,40]
console.log(numbers1.lastIndexOf(20))
